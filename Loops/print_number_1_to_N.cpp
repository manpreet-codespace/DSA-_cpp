#include <iostream>
using namespace std;
// -------------WHILE LOOP------------

int main(){

    int num = 1;
    int range;

    cin>>range;

    while(num <= range){
        cout<<num<<endl;
        num++;

    }

// ---------FOR LOOP-----------

    cin>> range;

    for(int num=1;num<=range;num++){
        cout<<num<<endl;
    }

    return 0;

}